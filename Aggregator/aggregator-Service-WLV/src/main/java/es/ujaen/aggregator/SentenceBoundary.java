package es.ujaen.aggregator;

public class SentenceBoundary {
	
	long startSentence;
	long endSentence;
	
	public long getStartSentence() {
		return startSentence;
	}
	
	public void setStartSentence(long startSentence) {
		this.startSentence = startSentence;
	}
	
	
	public long getEndSentence() {
		return endSentence;
	}
	public void setEndSentence(long endSentence) {
		this.endSentence = endSentence;
	}
	
	
	

}
