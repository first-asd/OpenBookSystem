package es.ujaen.sinai.first;

/***
 * The class stores the beginning and end offsets calculated by Gate for a token. 
 * @author Eduard
 */
public class GateOffset {
	
	
	private long begGateOffset=-1;
	private long endGateOffset=-1;
	
	
	public long getBegGateOffset() {
		return begGateOffset;
	}
	public void setBegGateOffset(long begGateOffset) {
		this.begGateOffset = begGateOffset;
	}
	public long getEndGateOffset() {
		return endGateOffset;
	}
	public void setEndGateOffset(long endGateOffset) {
		this.endGateOffset = endGateOffset;
	}
	
	
	
	
	

}
